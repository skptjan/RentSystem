package Classes;

import Interfaces.Observer;

public class DrillMachine extends Rent {
    protected String type;

    public DrillMachine(String type) {
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public double getPrice() {
        return price;
    }

    @Override
    public void setPrice(double price) {
        this.price = price;
    }
}
